#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	long	n;
	char	c;

	n = nbr;
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		c = (n % 10) + 48;
	}
	else
		c = n + 48;
	write(fd, &c, 1);
	return ;
}
